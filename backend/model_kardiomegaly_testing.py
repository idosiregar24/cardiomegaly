"""**Dibawah ini adalah perintah untuk Data Loader**"""

import tensorflow as tf
import os

# ==========================================
# 1. KONFIGURASI PARAMETER DATASET
# ==========================================
# Path menuju folder hasil split data yang sudah bersih
DIR_DATASET = "/content/drive/MyDrive/Semester 4/Penambangan Data/proyek_kardiomegali/data/dataset_split"
IMG_SIZE = (128, 128)
BATCH_SIZE = 32

print("--- Memuat Dataset Training ---")
train_ds = tf.keras.utils.image_dataset_from_directory(
    os.path.join(DIR_DATASET, 'train'),
    image_size=IMG_SIZE,
    batch_size=BATCH_SIZE,
    color_mode='grayscale', # Memaksa gambar dibaca dalam 1 channel (Hitam-Putih) untuk hemat RAM
    label_mode='binary',    # Label otomatis biner: 0 untuk normal, 1 untuk kardiomegali
    shuffle=True,           # Diacak agar distribusi data internal RSJ dan Kaggle merata
    seed=42
)

print("\n--- Memuat Dataset Validasi ---")
val_ds = tf.keras.utils.image_dataset_from_directory(
    os.path.join(DIR_DATASET, 'val'),
    image_size=IMG_SIZE,
    batch_size=BATCH_SIZE,
    color_mode='grayscale',
    label_mode='binary',
    shuffle=False           # Validasi tidak perlu diacak agar hasil evaluasi urut dan konsisten
)

# ==========================================
# 2. OPTIMASI PARALELISASI & MEMORI (TURBO)
# ==========================================
# Mempercepat loading gambar dari Google Drive ke RAM GPU secara otomatis
AUTOTUNE = tf.data.AUTOTUNE
train_ds = train_ds.cache().prefetch(buffer_size=AUTOTUNE)
val_ds = val_ds.cache().prefetch(buffer_size=AUTOTUNE)

print("\n✅ Data Loader siap! Data berhasil dimuat di memori dan dioptimasi.")

"""**DIBAWAH INI ADALAH PERINTAH UNTUK MEMBUAT MODEL**

Arsitektur Model CNN & Kompilasi
"""

from tensorflow.keras import layers, models

# ==========================================
# 1. MEMBANGUN MODEL CNN
# ==========================================
model = models.Sequential([
    # Rescaling piksel ke rentang 0-1
    layers.Rescaling(1./255, input_shape=(128, 128, 1)),

    # Blok 1: Deteksi fitur dasar (tepi dan garis)
    layers.Conv2D(32, (3, 3), activation='relu'),
    layers.MaxPooling2D((2, 2)),

    # Blok 2: Deteksi fitur tekstur dan bentuk jantung
    layers.Conv2D(64, (3, 3), activation='relu'),
    layers.MaxPooling2D((2, 2)),

    # Blok 3: Fitur morfometri yang lebih kompleks
    layers.Conv2D(128, (3, 3), activation='relu'),
    layers.MaxPooling2D((2, 2)),

    # Flatten & Dense Layer
    layers.Flatten(),
    layers.Dense(128, activation='relu'),
    layers.Dropout(0.5), # Mencegah ketergantungan berlebih pada data Kaggle (Overfitting)

    # Output Layer (Binary Classification via Sigmoid)
    layers.Dense(1, activation='sigmoid')
])

# Kompilasi dengan metrik medis: Recall sangat penting untuk screening agar tidak "miss" pasien sakit
model.compile(
    optimizer='adam',
    loss='binary_crossentropy',
    metrics=['accuracy', tf.keras.metrics.Recall(name='recall'), tf.keras.metrics.Precision(name='precision')]
)

model.summary()

"""**Training Model & Callback**

Kita akan memasang "rem otomatis" agar model berhenti berlatih jika akurasi sudah tidak naik lagi, serta menyimpan hasil terbaik secara otomatis ke Drive.
"""

from tensorflow.keras.callbacks import EarlyStopping, ModelCheckpoint

# Tentukan path penyimpanan model terbaik di Drive Anda
PATH_MODEL_BEST = "/content/drive/MyDrive/Semester 4/Penambangan Data/proyek_kardiomegali/models/cnn_kardiomegali_best.h5"

# Simpan model hanya jika performa validasi (val_loss) membaik
save_callback = ModelCheckpoint(filepath=PATH_MODEL_BEST, monitor='val_loss', save_best_only=True, verbose=1)

# Berhenti otomatis jika dalam 5 epoch tidak ada perbaikan
stop_callback = EarlyStopping(monitor='val_loss', patience=5, restore_best_weights=True)

print("--- Memulai Pelatihan Model ---")
history = model.fit(
    train_ds,
    validation_data=val_ds,
    epochs=25,
    callbacks=[save_callback, stop_callback]
)

"""**Evaluasi & Laporan Diagnostik**

Setelah selesai, kita perlu melihat seberapa akurat model dalam membedakan Kardiomegali vs Normal.
"""

import numpy as np
from sklearn.metrics import classification_report, confusion_matrix

# Ambil gambar dan label asli dari validation set
y_true = []
y_pred_prob = []

for images, labels in val_ds:
    y_true.extend(labels.numpy())
    preds = model.predict(images)
    y_pred_prob.extend(preds)

# Terapkan Threshold 0.5 sesuai spesifikasi Anda
y_pred = [1 if prob >= 0.5 else 0 for prob in y_pred_prob]

print("\n--- HASIL EVALUASI MODEL ---")
print(classification_report(y_true, y_pred, target_names=['Normal', 'Kardiomegali']))

"""**Fungsi Grad-CAM untuk Backend Penjelas**"""

import numpy as np
import tensorflow as tf
import cv2
import matplotlib.pyplot as plt

def make_gradcam_heatmap(img_array, model, last_conv_layer_name, pred_index=None):
    # 1. Buat model baru yang memetakan citra input ke aktivasi layer konvolusi terakhir & output prediksi
    grad_model = tf.keras.models.Model(
        inputs=[model.inputs],
        outputs=[model.get_layer(last_conv_layer_name).output, model.output]
    )

    # 2. Hitung gradien dari top kelas prediksi terhadap aktivasi layer konvolusi terakhir
    with tf.GradientTape() as tape:
        last_conv_layer_output, preds = grad_model(img_array)
        if pred_index is None:
            pred_index = tf.argmax(preds[0])
        class_channel = preds[:, pred_index]

    # 3. Ini adalah gradien dari kelas keluaran terhadap fungsionalitas layer konvolusi terakhir
    grads = tape.gradient(class_channel, last_conv_layer_output)

    # 4. Ambil rata-rata gradien secara spasial (global average pooling)
    pooled_grads = tf.reduce_mean(grads, axis=(0, 1, 2))

    # 5. Kalikan setiap saluran di feature map dengan "seberapa penting saluran ini" terhadap kelas target
    last_conv_layer_output = last_conv_layer_output[0]
    heatmap = last_conv_layer_output @ pooled_grads[..., tf.newaxis]
    heatmap = tf.squeeze(heatmap)

    # 6. Normalisasi heatmap antara 0 dan 1 untuk visualisasi, lalu terapkan ReLU
    heatmap = tf.maximum(heatmap, 0) / tf.reduce_max(heatmap)
    return heatmap.numpy()

def save_and_display_gradcam(img_path, heatmap, cam_path="cam.png", alpha=0.4):
    # Muat gambar asli
    img = cv2.imread(img_path)

    # Rescale heatmap ke ukuran gambar asli (misal kembali ke ukuran asli RSJ yang besar)
    heatmap = cv2.resize(heatmap, (img.shape[1], img.shape[0]))

    # Ubah heatmap menjadi rentang 0-255
    heatmap = np.uint8(255 * heatmap)

    # Gunakan skema warna JET untuk memberikan warna map panas (merah = panas/tinggi)
    heatmap = cv2.applyColorMap(heatmap, cv2.COLORMAP_JET)

    # Gabungkan (overlay) heatmap dengan gambar rontgen asli menggunakan transparansi alpha
    superimposed_img = heatmap * alpha + img
    superimposed_img = np.clip(superimposed_img, 0, 255).astype(np.uint8)

    # Simpan hasil visualisasi
    cv2.imwrite(cam_path, superimposed_img)
    return cam_path

