import os
import cv2
import pandas as pd
from sklearn.model_selection import train_test_split

# ==========================================
# 1. KONFIGURASI PATH & PARAMETER
# ==========================================
PATH_CSV = "/content/drive/MyDrive/Semester 4/Penambangan Data/proyek_kardiomegali/metadata/metadata_pasien_penambangan_data_rsj - Sheet1.csv"
DIR_RAW_INTERNAL = "/content/drive/MyDrive/Semester 4/Penambangan Data/proyek_kardiomegali/data/raw/internal_rsj"
DIR_RAW_KAGGLE = "/content/drive/MyDrive/Semester 4/Penambangan Data/proyek_kardiomegali/data/raw/kaggle_source"
DIR_OUTPUT = "/content/drive/MyDrive/Semester 4/Penambangan Data/proyek_kardiomegali/data/dataset_split"

TARGET_SIZE = (128, 128)
VAL_SIZE = 0.2
RANDOM_STATE = 42

for split in ['train', 'val']:
    for kelas in ['0_normal', '1_kardiomegali']:
        os.makedirs(os.path.join(DIR_OUTPUT, split, kelas), exist_ok=True)

# ==========================================
# 2. PROSES DATA INTERNAL (RSJ) -> PAKSA KE .PNG
# ==========================================
print("Mengompresi dan membagi data Internal RSJ...")
df = pd.read_csv(PATH_CSV)
df.dropna(subset=['label_kardiomegali'], inplace=True)
df['label_kardiomegali'] = df['label_kardiomegali'].astype(int)

train_df, val_df = train_test_split(
    df, test_size=VAL_SIZE, stratify=df['label_kardiomegali'], random_state=RANDOM_STATE
)

def process_and_save(dataframe, split_name):
    PROBABLE_EXTENSIONS = ['.png', '.jpg', '.jpeg', '.bmp', '.PNG', '.JPG', '.JPEG', '.BMP']

    for _, row in dataframe.iterrows():
        nama_file_dasar = str(row['nama_file_gambar']).strip()
        label = row['label_kardiomegali']
        sub_folder = "1_kardiomegali" if label == 1 else "0_normal"

        file_ditemukan = False
        path_asal = ""

        for ext in PROBABLE_EXTENSIONS:
            if nama_file_dasar.lower().endswith(ext.lower()):
                test_path = os.path.join(DIR_RAW_INTERNAL, nama_file_dasar)
            else:
                test_path = os.path.join(DIR_RAW_INTERNAL, f"{nama_file_dasar}{ext}")

            if os.path.exists(test_path):
                path_asal = test_path
                file_ditemukan = True
                break

        if file_ditemukan:
            # Hilangkan ekstensi lama jika ada di string nama_file_dasar
            nama_clean = nama_file_dasar
            for ext in PROBABLE_EXTENSIONS:
                if nama_clean.lower().endswith(ext.lower()):
                    nama_clean = nama_clean[:-len(ext)]
                    break

            # Paksa keluaran akhir menjadi berkstensi .png
            nama_file_final = f"{nama_clean.strip()}.png"
            path_tujuan = os.path.join(DIR_OUTPUT, split_name, sub_folder, nama_file_final)

            img = cv2.imread(path_asal, cv2.IMREAD_GRAYSCALE)
            if img is not None:
                img_resized = cv2.resize(img, TARGET_SIZE, interpolation=cv2.INTER_AREA)
                cv2.imwrite(path_tujuan, img_resized)
            else:
                print(f"Gagal membaca gambar: {path_asal}")
        else:
            print(f"Peringatan: File '{nama_file_dasar}' tidak ditemukan.")

process_and_save(train_df, 'train')
process_and_save(val_df, 'val')

